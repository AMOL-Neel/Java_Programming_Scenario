package stringex;

public class Test9
{
	public static void main(String[] args) 
	{
		String s = "hi sir how r u";
		
		// startswith()
		System.out.println(s.startsWith("hi"));
		
		// endswith()
		System.out.println(s.endsWith("u"));
		System.out.println();
		
		// contains()
		System.out.println(s.contains("sir"));
		System.out.println();
		
	}

	
}
